package Day03.循环结构;

/*
    输出1-10
 */
public class ForDemo2 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.print(i);
            System.out.print(" ");
        }
        System.out.println("\n---------------------------");
        for (int i = 0; i < 10; i++) {
            System.out.print(i+1);
            System.out.print(" ");
        }

    }
}
