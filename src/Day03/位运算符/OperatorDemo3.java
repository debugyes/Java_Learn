package Day03.位运算符;

public class OperatorDemo3 {
    public static void main(String[] args) {
        //<< 把<<左边的数据*2的移动次幂
        System.out.println(3 << 2); //3*2^2 = 3 * 4 = 12

        //>> 把<<左边的数据除以2的移动次幂
        System.out.println(24 >> 2); //24/2^2 = 24/4 = 6
        System.out.println(24 >>> 2);

        System.out.println(-24 >> 2);
        System.out.println(-24 >>> 2);

    }
}
