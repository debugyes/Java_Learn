package Day02.常量;
/*
    测试进制
 */
public class Constant2 {
    public static void main(String[] args) {
        //默认十进制
        System.out.println(100);
        //二进制
        System.out.println(0b100);
        //八进制
        System.out.println(0100);
        //十六进制
        System.out.println(0x100);
    }
}
